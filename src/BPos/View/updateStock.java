    private void qtyKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_qtyKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!((c >= '0' && c <= '9') || (c == '.' && !qty.getText().contains(".")))) {
            evt.consume();
        }
    }//GEN-LAST:event_qtyKeyTyped

