    public void loadPro(int stkid) {
        try {
            Session ses = BPos.Connection.NewHibernateUtil.getSessionFactory().openSession();
            BPos.DB.Stock stock = (BPos.DB.Stock) ses.load(BPos.DB.Stock.class, stkid);
            available.setText(stock.getQty() + "");

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

