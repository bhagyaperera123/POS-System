    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {

            if (!_Paid.getText().equals("")) {
                ArrayList<CustomerDue> ar = new ArrayList();

                Session ses = BPos.Connection.NewHibernateUtil.getSessionFactory().openSession();
                Transaction tr = ses.beginTransaction();
                Criteria cr = ses.createCriteria(BPos.DB.CustomerDue.class);
                cr.add(Restrictions.eq("customer", _Custom));
                List<BPos.DB.CustomerDue> l = cr.list();
                CustomerDue ccc = new CustomerDue();
                for (CustomerDue l1 : l) {
                    if (l1.getDueamount() != 0) {
                        l1.setDueamount(0.0);
                        ccc = l1;
                        ses.update(l1);
                        ar.add(l1);
                    }

                }
                BPos.DB.Customerduerecords cdr = new Customerduerecords();
                cdr.setCustomerDue(ccc);
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String date = sdf.format(new Date());
                cdr.setDatetime(date);
                cdr.setAmount(_Paid.getText());

                ses.save(cdr);
                tr.commit();
                ses.flush();
                ses.close();
                _Settings.LoadCUSDebit();
                this.dispose();
                JOptionPane.showMessageDialog(rootPane, "Payment Success!");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

