    public void loaddata() {
        Session ses = BPos.Connection.NewHibernateUtil.getSessionFactory().openSession();
        Criteria cr = ses.createCriteria(BPos.DB.CustomerDue.class);
        cr.add(Restrictions.eq("customer", _Custom));
        List<BPos.DB.CustomerDue> all = cr.list();
        double tot = 0;
        for (CustomerDue getall : all) {
            tot = tot + getall.getDueamount();
        }
        ses.close();
        TotDue.setText(tot + "");
        _Customer2.setText(_Custom.getName());
    }

